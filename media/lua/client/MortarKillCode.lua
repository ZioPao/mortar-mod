MortarKill = {}

function MortarKill.killzed(zed) 
	if not zed then return end
	local square = zed:getSquare() 
	for i=1,square:getObjects():size() do
		local object = square:getObjects():get(i-1)
		if instanceof(object, "IsoFire") and not object:isPermanent() and square:getModData()['mortarHit']  then
			if instanceof(zombie, "IsoZombie") then
				zed:Kill(zed); break
			end
		end
	end
end 
Events.OnZombieUpdate.Add(MortarKill.killzed)

function MortarKill.killplayer(pl) 
	if not pl then return end
	local square = pl:getSquare() 
	for i=1,square:getObjects():size() do
		local object = square:getObjects():get(i-1)
		if instanceof(object, "IsoFire") and not object:isPermanent() and square:getModData()['mortarHit'] then
			if instanceof(pl, "IsoGameCharacter") and pl:isOnFire() then
				pl:Kill(pl); break
			end
		end
	end
end 
Events.OnPlayerUpdate.Add(MortarKill.killplayer)