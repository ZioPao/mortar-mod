function MortarCheckers.isBomberValid()
    local pl = getSpecificPlayer(0)
    if isDriving() or getVehicle() then return false end
    if not getSpecificPlayer(0):isOutside() then return false end
    if pl:HasTrait('HardOfHearing') or pl:HasTrait('Deaf') then return false end
    if (pl:isRunning() or pl:isSprinting() and pl:isJustMoved()) then return false end
    if not pl:isAsleep() then return true end
    --TODO isHasFog --check if theres fog find the syntax  maybe  DayForecast() ? 
end

function MortarCheckers.isSpotterValid()
    local pl = getSpecificPlayer(0)
    if pl:isDriving() or pl:getVehicle() then return false end
    if pl:HasTrait('ShortSighted')  then return false end
    --if pl:isAiming() or pl:isSneaking() then return false end
    if (pl:isRunning() or pl:isSprinting() and pl:isJustMoved()) then return false end
    if not pl:isAsleep() then return true end
end
